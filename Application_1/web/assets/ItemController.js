const loadAllItem = () => {
    $.ajax({
        url: "http://localhost:8080/Application_1_war_exploded/item",
        type: "GET",
        success: (response) => {
            console.log(response);

            const tableBody = $('#itemTableBody');
            tableBody.empty();

            response.forEach(item => {
                tableBody.append(`
               <tr>
                    <td>${item.item_id}</td>
                    <td>${item.item_name}</td>
                    <td>${item.item_price}</td>
                    <td>${item.item_qty}</td>
                </tr>
                `)
            });
        },
        error: (err) => {
            console.log(err);
        }
    });
    autoGenerateNextId();
    clearField();
}

const loadItemTable = () => {
    $('#loadAllItemsBtn').click((e) => {
        loadAllItem();
    });
}

